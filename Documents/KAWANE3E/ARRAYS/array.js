const array = [21, 6, 84, 24, 86, 11]
console.log(array)
//Arrays -> métodos comuns

//Inserir dados
array.push(5) //Função que recebe parâmetro para acrescentar dados no final do array.
console.log(array)
array.unshift(5)  //Função que recebe parâmetro para acrescentar dados no início do array.
console.log(array)

//Remover dados
array.pop() //Função que remove dados no final do array. Perceba que ele não necessita receber parâmetros, pois não precisa saber qual elemento remover.
console.log(array)
array.shift() //Função que remove dados no início do array. Perceba que ele não necessita receber parâmetros, pois não precisa saber qual elemento remover.
console.log(array)
console.log(array)